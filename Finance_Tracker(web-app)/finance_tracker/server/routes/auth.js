// @route   PUT /api/auth/profile
// @desc    Update user profile
// @access  Private
router.put('/profile', auth, [
  body('name').optional().trim().isLength({ min: 2, max: 50 }).withMessage('Name must be between 2 and 50 characters'),
  body('preferences.currency').optional().isIn(['USD', 'EUR', 'GBP', 'INR', 'PKR', 'AED']).withMessage('Invalid currency'),
  body('preferences.theme').optional().isIn(['light', 'dark']).withMessage('Invalid theme'),
  body('preferences.language').optional().isIn(['en', 'es', 'fr', 'de', 'ar']).withMessage('Invalid language')
], async (req, res) => {
  try {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        message: 'Validation failed',
        errors: errors.array()
      });
    }

    const updates = req.body;
    const user = await User.findByIdAndUpdate(
      req.user._id,
      { $set: updates },
      { new: true, runValidators: true }
    );

    res.json({
      message: 'Profile updated successfully',
      user: {
        id: user._id,
        name: user.name,
        email: user.email,
        avatar: user.avatar,
        preferences: user.preferences
      }
    });
  } catch (error) {
    console.error('Profile update error:', error);
    res.status(500).json({
      message: 'Server error during profile update'
    });
  }
});

// @route   POST /api/auth/change-password
// @desc    Change user password
// @access  Private
router.post('/change-password', auth, [
  body('currentPassword').notEmpty().withMessage('Current password is required'),
  body('newPassword').isLength({ min: 6 }).withMessage('New password must be at least 6 characters long')
], async (req, res) => {
  try {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        message: 'Validation failed',
        errors: errors.array()
      });
    }

    const { currentPassword, newPassword } = req.body;

    // Get user with password
    const user = await User.findById(req.user._id).select('+password');
    
    // Check current password
    const isMatch = await user.comparePassword(currentPassword);
    if (!isMatch) {
      return res.status(400).json({
        message: 'Current password is incorrect'
      });
    }

    // Update password
    user.password = newPassword;
    await user.save();

    res.json({
      message: 'Password changed successfully'
    });
  } catch (error) {
    console.error('Password change error:', error);
    res.status(500).json({
      message: 'Server error during password change'
    });
  }
});

// @route   POST /api/auth/upload-avatar
// @desc    Upload profile picture
// @access  Private
router.post('/upload-avatar', auth, upload.single('avatar'), async (req, res) => {
  try {
    if (!req.file) {
      return res.status(400).json({
        message: 'No file uploaded'
      });
    }

    // Delete old avatar if exists
    const user = await User.findById(req.user._id);
    if (user.avatar && user.avatar !== '') {
      const oldAvatarPath = path.join(__dirname, '../uploads/avatars', path.basename(user.avatar));
      if (fs.existsSync(oldAvatarPath)) {
        fs.unlinkSync(oldAvatarPath);
      }
    }

    // Update user with new avatar path
    const avatarPath = `/uploads/avatars/${req.file.filename}`;
    const fullAvatarUrl = `${req.protocol}://${req.get('host')}${avatarPath}`;
    
    console.log('Uploaded file:', req.file);
    console.log('Avatar path:', avatarPath);
    console.log('Full avatar URL:', fullAvatarUrl);
    console.log('Full file path:', req.file.path);
    
    user.avatar = avatarPath;
    await user.save();
    
    console.log('User updated with avatar:', user.avatar);

    res.json({
      message: 'Avatar uploaded successfully',
      avatar: avatarPath,
      avatarUrl: fullAvatarUrl
    });
  } catch (error) {
    console.error('Avatar upload error:', error);
    res.status(500).json({
      message: 'Server error during avatar upload'
    });
  }
});

// @route   DELETE /api/auth/remove-avatar
// @desc    Remove profile picture
// @access  Private
router.delete('/remove-avatar', auth, async (req, res) => {
  try {
    const user = await User.findById(req.user._id);
    
    if (user.avatar && user.avatar !== '') {
      // Delete file from filesystem
      const avatarPath = path.join(__dirname, '../uploads/avatars', path.basename(user.avatar));
      if (fs.existsSync(avatarPath)) {
        fs.unlinkSync(avatarPath);
      }
      
      // Remove avatar from user record
      user.avatar = '';
      await user.save();
    }

    res.json({
      message: 'Avatar removed successfully'
    });
  } catch (error) {
    console.error('Avatar removal error:', error);
    res.status(500).json({
      message: 'Server error during avatar removal'
    });
  }
});

module.exports = router;
